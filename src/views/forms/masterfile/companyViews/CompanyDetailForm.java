    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            CompanyController controler = new CompanyController();
            Company _company = new Company();
            _company.setCompanyName(txtCompany.getText());
            _company.setBusinessStyle(txtBusinessStyle.getText());
            _company.setClassification(txtClassification.getText());
            _company.setAddress(txtAddress.getText());
            _company.setContactNo(txtContactNo.getText());
            _company.setId(Integer.valueOf(txtId.getText()));
            controler.UpdateCompany(_company);
            JOptionPane.showMessageDialog(null, txtCompany.getText() + " " +"is successfully saved!");
            viewOnly();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, txtCompany.getText() + " " +"is not updated!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            CompanyController controler = new CompanyController();
            controler.remove(Integer.valueOf(txtId.getText()));
            JOptionPane.showMessageDialog(null, "The record " +"is successfully removed!");
            clear();
            txtCompany.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "The record " +"is not removed!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

