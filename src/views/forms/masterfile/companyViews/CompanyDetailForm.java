    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        CompanyListForm _companyList = new CompanyListForm();
        int tempId = CompanyListForm.arryIdHolder[0];
        
        if (_companyList.isAddButtonClick == false){
            CompanyController cntrler = new CompanyController();
            String[] tempArry = new String[6];
    
                try {
                  for(int i = 0; i < tempArry.length;i++){
                    tempArry[i] = Array.get(cntrler.getCompanyById(tempId), i).toString();
                    }

                } catch (Exception e) { }
                txtId.setText(tempArry[0]);
                txtCompany.setText(tempArry[1]);
                txtBusinessStyle.setText(tempArry[2]);
                txtClassification.setText(tempArry[3]);
                txtAddress.setText(tempArry[4]);
                txtContactNo.setText(tempArry[5]);
                viewOnly();
        }
        else {
        readyToEdit();
        txtCompany.requestFocus();
        }
    }//GEN-LAST:event_formInternalFrameOpened

